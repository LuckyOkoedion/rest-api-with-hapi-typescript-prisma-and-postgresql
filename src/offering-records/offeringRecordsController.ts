import * as Hapi from "@hapi/hapi";
import { CreateOfferingRecords, UpdateOfferingRecords } from "./offering-records";
import { OfferingRecordsService } from "./offeringRecordsService";
import Boom  from "@hapi/boom";

export class OfferingRecordsController {

    public async create(request: Hapi.Request, h: Hapi.ResponseToolkit) {
        try {
            const requestBody: CreateOfferingRecords = request.payload as CreateOfferingRecords
            const result = await new OfferingRecordsService().create(requestBody);
            return h.response(result).code(201);
        } catch (error) {
            request.log("error", error);
            return Boom.badImplementation(JSON.stringify(error))
        }

    }


    public async getAll(request: Hapi.Request, h: Hapi.ResponseToolkit) {
        try {
            const result = await new OfferingRecordsService().getAll();
            return h.response(result).code(200);
        } catch (error) {
            request.log("error", error);
            return Boom.badImplementation(JSON.stringify(error))
        }
    }


    public async getById(request: Hapi.Request, h: Hapi.ResponseToolkit) {
        try {
            const id: number = +request.params.id;
            const result = await new OfferingRecordsService().getById(id);
            return h.response(result).code(200);
        } catch (error) {
            request.log("error", error);
            return Boom.badImplementation(JSON.stringify(error))
        }

    }


    public async update(request: Hapi.Request, h: Hapi.ResponseToolkit) {
        try {
            const id: number = +request.params.id;
            const requestBody: UpdateOfferingRecords = request.payload as UpdateOfferingRecords;
            const result = await new OfferingRecordsService().update(requestBody, id);
            return h.response(result).code(200);
        } catch (error) {
            request.log("error", error);
            return Boom.badImplementation(JSON.stringify(error))
        }


    }


    public async delete(request: Hapi.Request, h: Hapi.ResponseToolkit) {
        try {
            const id: number = +request.params.id;
            const result = await new OfferingRecordsService().delete(id);
            return h.response(result).code(200);
        } catch (error) {
            request.log("error", error);
            return Boom.badImplementation(JSON.stringify(error))
        }
    }
}